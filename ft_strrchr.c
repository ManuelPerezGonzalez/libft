#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	pos;

	pos = ft_strlen(s);
	while (pos > 0)
	{
		if ((unsigned char)s[pos] == (unsigned char)c)
			return ((char *)s + pos);
		pos--;
	}
	return (0);
}
